number1 = 2
number2 = 5
number3 = 5.666666

print(number2 / number1)  # 2.5
print(number2 // number1)  # 2
print(number1 ** number2)  # 32
print(pow(number1, number2))  # 32
print(max(number1, number2, number3))  # 5.666666
print(min(number1, number2, number3))  # 2
print(round(number3, 3))  # 5.667
print(float(number1))  # 2.0
print(int(number3))  # 5

